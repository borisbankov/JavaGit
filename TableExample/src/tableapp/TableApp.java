    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = jTextField1.getText();        
        String phone = jTextField2.getText();
        String city = jTextField3.getText();
        
        tableModel.addRow(new Object[] {name, phone, city});
        jTextField1.setText("");       
        jTextField2.setText("");
        jTextField3.setText("");

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selection = jTable1.getSelectedRow();
        if(selection > -1){
            String name = tableModel.getValueAt(selection, 0).toString();            
            String phone = tableModel.getValueAt(selection, 1).toString();            
            String city = tableModel.getValueAt(selection, 2).toString();

            listModel.addElement(name + " " + phone + " " + city); 
            tableModel.removeRow(selection);

        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jList1MouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseReleased
        String selection = jList1.getSelectedValue();
        String[] data = selection.split(" ");
        jTextField1.setText(data[0]);        
        jTextField2.setText(data[1]);
        jTextField3.setText(data[2]);
        listModel.remove(jList1.getSelectedIndex());

    }//GEN-LAST:event_jList1MouseReleased

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String name = jTextField4.getText();
//        int rowCount = tableModel.getRowCount();
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            if(!tableModel.getValueAt(i, 0).toString().contains(name)){
                tableModel.removeRow(i);
                i--;
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

